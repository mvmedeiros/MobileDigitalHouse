        // Inflate the layout for this fragment
        val view = inflater.inflate(R.layout.fragment_sign_in, container, false)

        val signInUsername  = view.findViewById<TextInputEditText>(R.id.etSignInUsername)
        val signInPassword = view.findViewById<TextInputEditText>(R.id.etSignInPassword)
        val signInButton = view.findViewById<Button>(R.id.btnLogin)

        signInButton.setOnClickListener{

            val user = logIn(signInUsername.text.toString(), signInPassword.text.toString())

